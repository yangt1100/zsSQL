package com.zssql.common.enums;

/**
 * 值编辑类型
 */
public enum ValEditType {
    // 字符串
    STRING,
    // 大字段
    BLOB,
    // 枚举
    ENUM,
    // SET
    SET,
    // 整数
    INTEGER,
    // 正整数
    U_INTEGER,
    // 小数
    DECIMAL,
    // 正小数
    U_DECIMAL,
    // 日期
    DATE,
    // 日期时间
    DATETIME,
    // 时间
    TIME,
    // 未知
    UNKNOWN
}
